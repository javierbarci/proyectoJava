    private void crearProducto(){
        Producto p = new Producto();
        List<Producto> todosProductos = new ArrayList<>();
        todosProductos = service.getProductos();
        p.setIdProducto(buscarMayorInt(todosProductos)+1);
        p.setIdUsuario(this.usuario.getIdUsuario());
        p.setNombre(txtNombreProducto.getText());
        p.setDescripcion(txaDescripcion.getText());
        p.setValoracion(jsValoracion.getValue());
        Categoria c = (Categoria) cbxCategoria.getSelectedItem();
        p.setCategoria(c.getIdCategoria());
        p.setPrecio(Double.parseDouble(String.valueOf(ftxPrecio.getValue())));
        if (f != null) {
            try {
                p.setImagen(f.getCanonicalPath());
            } catch (IOException ex) {
                Logger.getLogger(MisProductos.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            p.setImagen("images/notfound.png");
        }
        p.setFechaAlta( (Date) ftxFechaAlta.getValue());
        p.setCantidad(Integer.parseInt(txtCantidad.getText()));
        service.nuevoProducto(p);
        productos = service.getProductosUsuario(this.usuario);
        Collections.sort(productos, new OrdenarProductosPorNombre());
        /*int i;
        for(i=0;i<productos.size() 
                && productos.get(i).getIdProducto() != p.getIdProducto();i++); //NO ENCUENTRA EL PRODUCTO y no sé dónde lo pone
        pos = i;*/
        pos = 1;
        mostrar();
        cargarCombos();
    }
    
    private boolean comprobarCamposVacios(){
        if (txtNombreProducto.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, 
                "Debe especificarse el nombre del producto",
                "Faltan datos",
                JOptionPane.ERROR_MESSAGE);
            txtNombreProducto.requestFocus();
            return true;
        } else if (txaDescripcion.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, 
                "Debe especificarse la descripción",
                "Faltan datos",
                JOptionPane.ERROR_MESSAGE);
            txaDescripcion.requestFocus();
            return true;
        } else if (ftxPrecio.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, 
                "Debe especificarse el precio",
                "Faltan datos",
                JOptionPane.ERROR_MESSAGE);
            ftxPrecio.requestFocus();
            return true;
        } else if (txtCantidad.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, 
                "Debe especificarse la cantidad",
                "Faltan datos",
                JOptionPane.ERROR_MESSAGE);
            txtCantidad.requestFocus();
            return true;
        } else {
            return false;
        }
    }
    
    private int buscarMayorInt(List<Producto> lista){
        int mayor = -1;
        for (int i=0; i<lista.size(); i++){
            if (lista.get(i).getIdProducto() > mayor){
                mayor = lista.get(i).getIdProducto();
            }
        }
        return mayor;
    }
    
