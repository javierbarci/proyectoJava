    private void cambiarStock(int pos){
        if(productos.get(pos).getCantidad() > 0){
            lblStock.setText("En stock.");
        } else {
            lblStock.setText("Fuera de stock.");
        }
    }
    
    private void cargarCombos() {
        
        for (Producto p : productos) {
            cbxProductos.addItem(p);
        }
        if (!productos.isEmpty()){
            cbxProductos.setSelectedIndex(0);
        }
        
        for (Categoria c : categorias) {
            cbxCategoria.addItem(c);
        }
        if (!productos.isEmpty()) {
            int idCategoria = productos.get(pos-1).getCategoria();
            cbxCategoria.setSelectedItem(service.getCategoria(idCategoria));
        } else {
            cbxCategoria.setSelectedIndex(0);
        }
    }
    
    private void eliminarProducto(){
        Producto p = productos.get(pos-1);
        cbxProductos.removeItem(p);
        service.eliminarProducto(p);
        mostrar();
        
    }
    
