    private void treCategoriasValueChanged(javax.swing.event.TreeSelectionEvent evt) {//GEN-FIRST:event_treCategoriasValueChanged
        TreePath path = evt.getPath();
        if (path.getPathCount() == 2) {
            DefaultMutableTreeNode treCategorias = (DefaultMutableTreeNode) path.getLastPathComponent();
            Categoria c = (Categoria) treCategorias.getUserObject();
            cargarProductosCategoria(c);
        }
    }//GEN-LAST:event_treCategoriasValueChanged
    
