    private void crearArbolCategorias(){
        trRaiz = new DefaultMutableTreeNode("Categorías");
        for (Categoria c : service.getCategorias()){
            DefaultMutableTreeNode trCat = new DefaultMutableTreeNode(c);
            trRaiz.add(trCat);
        }
        TreeModel model = new DefaultTreeModel(trRaiz);
        treCategorias.setModel(model);
    }
    
  
    
    class CategoriasRenderer implements TreeCellRenderer{

        @Override
        public Component getTreeCellRendererComponent(JTree tree, Object value, boolean selected, boolean expanded, boolean leaf, int row, boolean hasFocus) {
            JLabel lbl = new JLabel();
            DefaultMutableTreeNode nodo = (DefaultMutableTreeNode) value;
            if (row == 0){
                lbl.setText("Categoría");
                Icon ic = IconFontSwing.buildIcon(FontAwesome.ADDRESS_BOOK, 20);
                lbl.setIcon(ic);
            } else {
                lbl.setText(nodo.getUserObject().toString());
                if (nodo.getUserObject() instanceof Categoria) {
                    Categoria c = (Categoria) nodo.getUserObject();
                    switch (c.getIdCategoria()) {
                        case 1:
                            ImageIcon ic = foto.establecerFotoCategoriaFactor(c,20.0);
                            lbl.setIcon(ic);
                            break;
                        case 2:
                            ic = foto.establecerFotoCategoriaFactor(c,20.0);
                            lbl.setIcon(ic);
                            break;
                        case 3:
                            ic = foto.establecerFotoCategoriaFactor(c,20.0);
                            lbl.setIcon(ic);
                            break;
                        default:
                            ic = foto.establecerFotoCategoriaFactor(c,20.0);
                            lbl.setIcon(ic);
                            break;
                    }
                }
            }
            return lbl;
        }
    }
    
    
    
