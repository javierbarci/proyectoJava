    private void login(){
        Usuario u = null;
        if (txtUsuario.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, 
                "Debe especificarse el usuario",
                "Faltan datos",
                JOptionPane.ERROR_MESSAGE);
            txtUsuario.requestFocus();
        } else if (pwdPassword.getPassword().length == 0){
            JOptionPane.showMessageDialog(this, 
                "Debe especificarse la contrase침a",
                "Faltan datos",
                JOptionPane.ERROR_MESSAGE);
            pwdPassword.requestFocus();
        } else {
            String pwd = new String(pwdPassword.getPassword());
            u = service.login(txtUsuario.getText(), pwd);
            if (u == null){
                lblError.setText("Usuario o contrase침a err칩neos");
                pwdPassword.setText("");
                pwdPassword.requestFocus();
            } else {
                new Principal(u).setVisible(true);
                this.dispose();
            }
        }
    }
    
